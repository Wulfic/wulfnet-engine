FluidSurface::FluidSurface() = default;

FluidSurface::~FluidSurface() {
    Shutdown();
}

// =============================================================================
// Initialization
// =============================================================================

bool FluidSurface::Initialize(const FluidSurfaceConfig& config, VulkanContext* vulkan) {
    if (m_initialized) return false;

    m_config = config;
    m_vulkanContext = vulkan;
    m_gpuEnabled = (vulkan != nullptr) && config.useGPU;

    // Allocate density grid
    m_gridTotalCells = config.gridSizeX * config.gridSizeY * config.gridSizeZ;
    m_density.resize(m_gridTotalCells, 0.0f);

    // Reserve mesh storage
    m_vertices.reserve(m_gridTotalCells);
    m_triangles.reserve(m_gridTotalCells);
    m_indices.reserve(m_gridTotalCells * 3);

    m_initialized = true;
    return true;
}

void FluidSurface::Shutdown() {
    m_density.clear();
    m_vertices.clear();
    m_triangles.clear();
    m_indices.clear();

    m_vulkanContext = nullptr;
    m_gpuEnabled = false;
    m_initialized = false;
}

// =============================================================================
// Grid Helpers
// =============================================================================

int FluidSurface::GridIndex(int i, int j, int k) const {
    return i + j * m_config.gridSizeX + k * m_config.gridSizeX * m_config.gridSizeY;
}

bool FluidSurface::InBounds(int i, int j, int k) const {
    return i >= 0 && i < static_cast<int>(m_config.gridSizeX) &&
           j >= 0 && j < static_cast<int>(m_config.gridSizeY) &&
           k >= 0 && k < static_cast<int>(m_config.gridSizeZ);
}

void FluidSurface::WorldToGrid(float wx, float wy, float wz, float& gx, float& gy, float& gz) const {
    gx = wx / m_config.cellSize;
    gy = wy / m_config.cellSize;
    gz = wz / m_config.cellSize;
}

void FluidSurface::GridToWorld(int i, int j, int k, float& wx, float& wy, float& wz) const {
    wx = i * m_config.cellSize;
    wy = j * m_config.cellSize;
    wz = k * m_config.cellSize;
}

float FluidSurface::GetDensity(int i, int j, int k) const {
    if (!InBounds(i, j, k)) return 0.0f;
    return m_density[GridIndex(i, j, k)];
}

void FluidSurface::SetDensity(int i, int j, int k, float value) {
    if (InBounds(i, j, k)) {
        m_density[GridIndex(i, j, k)] = value;
    }
}

// =============================================================================
// Surface Generation
// =============================================================================

void FluidSurface::GenerateSurface(const COFLIPSystem& fluid) {
    if (!m_initialized) return;

    auto startTime = std::chrono::high_resolution_clock::now();

    // Clear density
    auto splatStart = std::chrono::high_resolution_clock::now();
    ClearDensity();

    // Splat particles to density grid
    const auto& particles = fluid.GetParticles();
    uint32_t activeCount = fluid.GetActiveParticleCount();

    for (uint32_t p = 0; p < activeCount; ++p) {
        const auto& part = particles[p];
        if (part.flags & 1) {
            SplatParticle(part.x, part.y, part.z, 1.0f);
        }
    }
    auto splatEnd = std::chrono::high_resolution_clock::now();
    m_stats.splatTimeMs = std::chrono::duration<float, std::milli>(splatEnd - splatStart).count();

    // Smooth density field
    auto smoothStart = std::chrono::high_resolution_clock::now();
    SmoothDensity();
    auto smoothEnd = std::chrono::high_resolution_clock::now();
    m_stats.smoothTimeMs = std::chrono::duration<float, std::milli>(smoothEnd - smoothStart).count();

    // Extract surface via marching cubes
    auto mcStart = std::chrono::high_resolution_clock::now();
    ExtractSurface();
    auto mcEnd = std::chrono::high_resolution_clock::now();
    m_stats.marchingCubesTimeMs = std::chrono::duration<float, std::milli>(mcEnd - mcStart).count();

    auto endTime = std::chrono::high_resolution_clock::now();
    m_stats.totalTimeMs = std::chrono::duration<float, std::milli>(endTime - startTime).count();

    m_stats.vertexCount = static_cast<uint32_t>(m_vertices.size());
    m_stats.triangleCount = static_cast<uint32_t>(m_triangles.size());
}

void FluidSurface::ClearDensity() {
    std::fill(m_density.begin(), m_density.end(), 0.0f);
}

void FluidSurface::SplatParticle(float x, float y, float z, float weight) {
    float gx, gy, gz;
    WorldToGrid(x, y, z, gx, gy, gz);

    float radius = m_config.splatRadius;
    float sigma = m_config.smoothingSigma;
    float sigma2 = sigma * sigma;

    int iMin = std::max(0, static_cast<int>(gx - radius));
    int iMax = std::min(static_cast<int>(m_config.gridSizeX) - 1, static_cast<int>(gx + radius));
    int jMin = std::max(0, static_cast<int>(gy - radius));
    int jMax = std::min(static_cast<int>(m_config.gridSizeY) - 1, static_cast<int>(gy + radius));
    int kMin = std::max(0, static_cast<int>(gz - radius));
    int kMax = std::min(static_cast<int>(m_config.gridSizeZ) - 1, static_cast<int>(gz + radius));

    for (int k = kMin; k <= kMax; ++k) {
        for (int j = jMin; j <= jMax; ++j) {
            for (int i = iMin; i <= iMax; ++i) {
                float dx = i - gx;
                float dy = j - gy;
                float dz = k - gz;
                float dist2 = dx*dx + dy*dy + dz*dz;

                // Gaussian kernel
                float w = weight * std::exp(-dist2 / (2.0f * sigma2));
                m_density[GridIndex(i, j, k)] += w;
            }
        }
    }
}

void FluidSurface::SmoothDensity() {
    // Simple 3x3x3 box blur for smoothing
    std::vector<float> temp(m_gridTotalCells, 0.0f);

    for (uint32_t k = 1; k < m_config.gridSizeZ - 1; ++k) {
        for (uint32_t j = 1; j < m_config.gridSizeY - 1; ++j) {
            for (uint32_t i = 1; i < m_config.gridSizeX - 1; ++i) {
                float sum = 0;
                for (int dk = -1; dk <= 1; ++dk) {
                    for (int dj = -1; dj <= 1; ++dj) {
                        for (int di = -1; di <= 1; ++di) {
                            sum += m_density[GridIndex(i + di, j + dj, k + dk)];
                        }
                    }
                }
                temp[GridIndex(i, j, k)] = sum / 27.0f;
            }
        }
    }

    m_density = std::move(temp);
}

void FluidSurface::ExtractSurface() {
    m_vertices.clear();
    m_triangles.clear();
    m_indices.clear();

    // Process each cell
    for (uint32_t k = 0; k < m_config.gridSizeZ - 1; ++k) {
        for (uint32_t j = 0; j < m_config.gridSizeY - 1; ++j) {
            for (uint32_t i = 0; i < m_config.gridSizeX - 1; ++i) {
                ProcessCell(i, j, k);
            }
        }
    }
}

void FluidSurface::ProcessCell(int i, int j, int k) {
    using namespace MarchingCubesTables;

    float iso = m_config.isoLevel;

    // Get density values at 8 corners
    float d[8];
    d[0] = GetDensity(i, j, k);
    d[1] = GetDensity(i + 1, j, k);
    d[2] = GetDensity(i + 1, j + 1, k);
    d[3] = GetDensity(i, j + 1, k);
    d[4] = GetDensity(i, j, k + 1);
    d[5] = GetDensity(i + 1, j, k + 1);
    d[6] = GetDensity(i + 1, j + 1, k + 1);
    d[7] = GetDensity(i, j + 1, k + 1);

    // Compute cube index
    int cubeIndex = 0;
    if (d[0] < iso) cubeIndex |= 1;
    if (d[1] < iso) cubeIndex |= 2;
    if (d[2] < iso) cubeIndex |= 4;
    if (d[3] < iso) cubeIndex |= 8;
    if (d[4] < iso) cubeIndex |= 16;
    if (d[5] < iso) cubeIndex |= 32;
    if (d[6] < iso) cubeIndex |= 64;
    if (d[7] < iso) cubeIndex |= 128;

    // Check if fully inside or outside
    if (EdgeTable[cubeIndex] == 0) return;

    // Compute edge vertices
    float vertList[12][3];

    // Edge 0: between corners 0 and 1
    if (EdgeTable[cubeIndex] & 1)
        InterpolateVertex(i, j, k, i + 1, j, k, vertList[0][0], vertList[0][1], vertList[0][2]);
    // Edge 1: between corners 1 and 2
    if (EdgeTable[cubeIndex] & 2)
        InterpolateVertex(i + 1, j, k, i + 1, j + 1, k, vertList[1][0], vertList[1][1], vertList[1][2]);
    // Edge 2: between corners 2 and 3
    if (EdgeTable[cubeIndex] & 4)
        InterpolateVertex(i + 1, j + 1, k, i, j + 1, k, vertList[2][0], vertList[2][1], vertList[2][2]);
    // Edge 3: between corners 3 and 0
    if (EdgeTable[cubeIndex] & 8)
        InterpolateVertex(i, j + 1, k, i, j, k, vertList[3][0], vertList[3][1], vertList[3][2]);
    // Edge 4: between corners 4 and 5
    if (EdgeTable[cubeIndex] & 16)
        InterpolateVertex(i, j, k + 1, i + 1, j, k + 1, vertList[4][0], vertList[4][1], vertList[4][2]);
    // Edge 5: between corners 5 and 6
    if (EdgeTable[cubeIndex] & 32)
        InterpolateVertex(i + 1, j, k + 1, i + 1, j + 1, k + 1, vertList[5][0], vertList[5][1], vertList[5][2]);
    // Edge 6: between corners 6 and 7
    if (EdgeTable[cubeIndex] & 64)
        InterpolateVertex(i + 1, j + 1, k + 1, i, j + 1, k + 1, vertList[6][0], vertList[6][1], vertList[6][2]);
    // Edge 7: between corners 7 and 4
    if (EdgeTable[cubeIndex] & 128)
        InterpolateVertex(i, j + 1, k + 1, i, j, k + 1, vertList[7][0], vertList[7][1], vertList[7][2]);
    // Edge 8: between corners 0 and 4
    if (EdgeTable[cubeIndex] & 256)
        InterpolateVertex(i, j, k, i, j, k + 1, vertList[8][0], vertList[8][1], vertList[8][2]);
    // Edge 9: between corners 1 and 5
    if (EdgeTable[cubeIndex] & 512)
        InterpolateVertex(i + 1, j, k, i + 1, j, k + 1, vertList[9][0], vertList[9][1], vertList[9][2]);
    // Edge 10: between corners 2 and 6
    if (EdgeTable[cubeIndex] & 1024)
        InterpolateVertex(i + 1, j + 1, k, i + 1, j + 1, k + 1, vertList[10][0], vertList[10][1], vertList[10][2]);
    // Edge 11: between corners 3 and 7
    if (EdgeTable[cubeIndex] & 2048)
        InterpolateVertex(i, j + 1, k, i, j + 1, k + 1, vertList[11][0], vertList[11][1], vertList[11][2]);

    // Generate triangles
    for (int n = 0; TriTable[cubeIndex][n] != -1; n += 3) {
        FluidSurfaceVertex v0, v1, v2;

        int e0 = TriTable[cubeIndex][n];
        int e1 = TriTable[cubeIndex][n + 1];
        int e2 = TriTable[cubeIndex][n + 2];

        v0.x = vertList[e0][0]; v0.y = vertList[e0][1]; v0.z = vertList[e0][2];
        v1.x = vertList[e1][0]; v1.y = vertList[e1][1]; v1.z = vertList[e1][2];
        v2.x = vertList[e2][0]; v2.y = vertList[e2][1]; v2.z = vertList[e2][2];

        // Compute normals from density gradient
        ComputeNormal(v0.x, v0.y, v0.z, v0.nx, v0.ny, v0.nz);
        ComputeNormal(v1.x, v1.y, v1.z, v1.nx, v1.ny, v1.nz);
        ComputeNormal(v2.x, v2.y, v2.z, v2.nx, v2.ny, v2.nz);

        // Simple UV (planar projection)
        v0.u = v0.x; v0.v = v0.z;
        v1.u = v1.x; v1.v = v1.z;
        v2.u = v2.x; v2.v = v2.z;

        uint32_t baseIdx = static_cast<uint32_t>(m_vertices.size());
        m_vertices.push_back(v0);
        m_vertices.push_back(v1);
        m_vertices.push_back(v2);

        FluidSurfaceTriangle tri;
        tri.v0 = baseIdx;
        tri.v1 = baseIdx + 1;
        tri.v2 = baseIdx + 2;
        m_triangles.push_back(tri);

        m_indices.push_back(baseIdx);
        m_indices.push_back(baseIdx + 1);
        m_indices.push_back(baseIdx + 2);
    }
}

void FluidSurface::InterpolateVertex(int i1, int j1, int k1, int i2, int j2, int k2,
                                     float& vx, float& vy, float& vz) const {
    float iso = m_config.isoLevel;
    float d1 = GetDensity(i1, j1, k1);
    float d2 = GetDensity(i2, j2, k2);

    float t = 0.5f;
    if (std::abs(d2 - d1) > 1e-6f) {
        t = (iso - d1) / (d2 - d1);
    }
    t = std::max(0.0f, std::min(1.0f, t));

    float wx1, wy1, wz1, wx2, wy2, wz2;
    GridToWorld(i1, j1, k1, wx1, wy1, wz1);
    GridToWorld(i2, j2, k2, wx2, wy2, wz2);

    vx = wx1 + t * (wx2 - wx1);
    vy = wy1 + t * (wy2 - wy1);
    vz = wz1 + t * (wz2 - wz1);
}

void FluidSurface::ComputeNormal(float x, float y, float z, float& nx, float& ny, float& nz) const {
    float gx, gy, gz;
    WorldToGrid(x, y, z, gx, gy, gz);

    int i = static_cast<int>(gx);
    int j = static_cast<int>(gy);
    int k = static_cast<int>(gz);

    // Gradient of density field (points outward from fluid)
    nx = GetDensity(i + 1, j, k) - GetDensity(i - 1, j, k);
    ny = GetDensity(i, j + 1, k) - GetDensity(i, j - 1, k);
    nz = GetDensity(i, j, k + 1) - GetDensity(i, j, k - 1);

    // Normalize (invert because we want outward normals)
    float len = std::sqrt(nx*nx + ny*ny + nz*nz);
    if (len > 1e-6f) {
        nx = -nx / len;
        ny = -ny / len;
        nz = -nz / len;
    } else {
        nx = 0; ny = 1; nz = 0;
    }
}

